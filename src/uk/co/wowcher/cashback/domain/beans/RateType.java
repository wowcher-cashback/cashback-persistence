package uk.co.wowcher.cashback.domain.beans;




public class RateType {

	private int iRateTypeId;
	private String sRateTypeCode;
	private String sRateTypeName;
	private String sRateTypeDescription;
	public int getiRateTypeId() {
		return iRateTypeId;
	}
	public void setiRateTypeId(int iRateTypeId) {
		this.iRateTypeId = iRateTypeId;
	}
	public String getsRateTypeCode() {
		return sRateTypeCode;
	}
	public void setsRateTypeCode(String sRateTypeCode) {
		this.sRateTypeCode = sRateTypeCode;
	}
	public String getsRateTypeName() {
		return sRateTypeName;
	}
	public void setsRateTypeName(String sRateTypeName) {
		this.sRateTypeName = sRateTypeName;
	}
	public String getsRateTypeDescription() {
		return sRateTypeDescription;
	}
	public void setsRateTypeDescription(String sRateTypeDescription) {
		this.sRateTypeDescription = sRateTypeDescription;
	}
	
	
	
}
